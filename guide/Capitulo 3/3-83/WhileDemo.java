/**
 * Demuestra el uso del bucle While
 * WhileDemo.java
 */
class WhileDemo {
    public static void main(String args[]) {
        char ch;

        //imprime el alfabeto usando un bucle while
        ch = 'a';
        while (ch <= 'z'){
            System.out.println(ch);
            ch++;
        }
        
    }
}