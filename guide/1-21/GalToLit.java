public class GalToLit {
    public static void main(String args[]) {
        double gallons; //almacena los galones
        double liters; //almacena los litros

        gallons = 10; //comienza con 10 galones
        liters = gallons * 3.7854; //convierte a litros

        System.out.println(gallons + " galones son " + liters + " litros.");
        
    }

    
}